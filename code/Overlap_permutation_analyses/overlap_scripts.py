def count_lines(filename):
    with open(filename, 'r') as file:
        return sum(1 for _ in file)

def overlap_count(peaks, rbp):
    
    proc = subprocess.run([
        "bedtools", "intersect", "-u", "-s", "-a", str(peaks), "-b", str(rbp)],
                          capture_output=True, text=True, check=True)
    return len(proc.stdout.strip().splitlines()) if proc.stdout.strip() else 0


def overlap_diff(TRIBE_BED, CONTROL_BED, ref_dir, save_csv=None):
    """
    Calculate overlap counts between TRIBE/CONTROL BEDs and a set of reference BEDs.

    For each BED file in ref_dir, computes the number of overlaps with TRIBE_BED and CONTROL_BED,
    and calculates the difference in counts. Results are returned as a pandas DataFrame,
    and can optionally be saved as a CSV file.

    Parameters
    ----------
    TRIBE_BED : str or Path
        Path to the TRIBE BED file.
    CONTROL_BED : str or Path
        Path to the CONTROL BED file.
    ref_dir : Path or str
        Directory containing reference BED files to compare against.
    save_csv : bool or str or Path, optional
        If provided, saves the resulting DataFrame to a CSV file.
        If True, saves to a default name in the ref_dir.
        If a string or Path, saves to the given file path.

    Returns
    -------
    obs_df : pandas.DataFrame
        DataFrame with columns: RBP, <TRIBE_obs_col>, obs_CONTROL, obs_diff
        (where <TRIBE_obs_col> is derived from TRIBE_BED's basename prefix).
    """
    obs_records = []
    for rbp_bed in ref_dir.glob("*.bed"):
        rbp_name = rbp_bed.stem
        obs_tribe = overlap_count(TRIBE_BED, rbp_bed)
        obs_ctrl = overlap_count(CONTROL_BED, rbp_bed)
        obs_diff  = obs_tribe - obs_ctrl
        obs_records.append({
            'RBP': rbp_name,
            f'obs_{os.path.basename(TRIBE_BED).split("_")[0]}': obs_tribe,
            f'obs_CONTROL': obs_ctrl,
            'obs_diff': obs_diff
        })
    obs_df = pd.DataFrame(obs_records)
    if save_csv:
        # Work out auto filename if needed
        if isinstance(save_csv, (str, os.PathLike)):
            csv_path = Path(save_csv)
        else:
            prefix = os.path.basename(TRIBE_BED).split("_")[0]
            csv_path = Path(ref_dir) / f"overlap_diff_{prefix}.csv"
        csv_path.parent.mkdir(parents=True, exist_ok=True)  # Ensure dir exists
        obs_df.to_csv(str(csv_path), index=False)
        print(f"Saved overlap difference results to: {csv_path}")
    return obs_df



def empirical_p(obs_diff, perm_diffs):
    # One-sided Test > Control
    extreme = sum(p >= obs_diff for p in perm_diffs)
    return (extreme + 1) / (len(perm_diffs) + 1)



def overlap_stats(obs_df, permdiff_dir):
    
    perm_diffs = {}
    for rbp in obs_df['RBP']:
        with open(f"{permdiff_dir}/{rbp}_permdiff.txt") as f:
            vals = [int(x.strip()) for x in f]
        perm_diffs[rbp] = vals
    
    obs_df['p_empirical'] = [empirical_p(o, perm_diffs[r]) for o, r in zip(obs_df['obs_diff'], obs_df['RBP'])]
    obs_df['FDR_BH'] = multipletests(obs_df['p_empirical'], method='fdr_bh')[1]
    obs_df = obs_df.sort_values('FDR_BH')

    return obs_df



def process_permdiff(obs_df, permdiff_dir, TRIBE_BED, CONTROL_BED):
    """
    Augments the observed overlap DataFrame with percent overlaps and permutation difference statistics.
    
    This function calculates the percent overlap of each RBP in both the TRIBE and CONTROL datasets,
    aggregates permutation differences from permdiff files, merges these with the observed overlaps
    and saves the final table as a CSV. The CSV is named after the TRIBE and CONTROL datasets.

    Parameters
    ----------
    obs_df : pandas.DataFrame
        DataFrame with overlap counts per RBP, typically generated by overlap_diff.
    permdiff_dir : str or Path
        Directory containing per-RBP permutation difference files named '<RBP>_permdiff.txt'.
        Each file should contain one value per line (permutation difference).
    TRIBE_BED : str or Path
        Path to the TRIBE BED file (used to count total rows for percent calculations).
    CONTROL_BED : str or Path
        Path to the CONTROL BED file (used to count total rows for percent calculations).

    Returns
    -------
    obs_df : pandas.DataFrame
        DataFrame with added percent overlap columns, permutation stats, and all permutation data as columns.
        Also writes this DataFrame to a CSV file named '<TRIBE_NAME>_<CONTROL_NAME>_permdiff.csv'.
    """
    TRIBE_name = obs_df.columns[1].replace('obs_', '', 1)
    CONTROL_name = obs_df.columns[2].replace('obs_', '', 1)
    
    # Add columns for %overlap in each data set
    obs_df[f'{TRIBE_name}_overlap'] = obs_df[f'obs_{TRIBE_name}']/count_lines(TRIBE_BED)*100
    obs_df[f'{CONTROL_name}_overlap'] = obs_df[f'obs_{CONTROL_name}']/count_lines(CONTROL_BED)*100
    
    # Get the stats
    obs_df = overlap_stats(obs_df, permdiff_dir)
    
    # Add all the permutation data in columns for graphing
    rows = []
    for rbp in obs_df['RBP']:
        # Get permutation difference distribution in PERCENT (% IFIT3 overlap - % Control overlap)
        perm_file = f'{permdiff_dir}/{rbp}_permdiff.txt'
        with open(perm_file) as f:
            permdiffs = [int(x.strip()) for x in f]
            row_dict = {'RBP': rbp}
            row_dict.update({f'P_{idx+1}': num for idx, num in enumerate(permdiffs)})
            rows.append(row_dict)
    permdiff_df = pd.DataFrame(rows)
    obs_df = obs_df.merge(permdiff_df, on='RBP')

    obs_df.to_csv(f'{permdiff_dir}/{TRIBE_name}_{CONTROL_name}_permdiff.csv', index=False)
    print(f'{TRIBE_name}_{CONTROL_name}_permdiff.csv saved to {permdiff_dir}')
    
    return obs_df


def clusters(bed, out_dir, dist, steps=None):
    """
    Run bedtools cluster on one or more BED files using given distance(s).
    
    Parameters:
        bed (str or Path): Path to a BED file or a directory containing .bed files.
        out_dir (str or Path): Directory to write clustered output files.
        dist (int): Maximum distance for clustering (-d).
        steps (int or None): Step size to incrementally run clustering up to dist.
                             If None, runs only with `dist`.
    """
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    
    # Accept a single file or a directory/glob
    bed_paths = []
    bed = Path(bed)
    if bed.is_dir():
        bed_paths = list(bed.glob("*.bed"))
    elif '*' in str(bed) or '?' in str(bed):
        bed_paths = list(bed.parent.glob(bed.name))
    else:
        bed_paths = [bed]
    
    distances = []
    if steps is not None and steps > 0 and dist >= steps:
        # Create list like [steps, steps*2, ..., dist]
        distances = list(range(steps, dist+1, steps))
    else:
        distances = [dist]
    
    for bed_file in bed_paths:
        bed_name = bed_file.stem
        for d in distances:
            proc = subprocess.run([
                    "bedtools", "cluster", "-s", "-d", str(d), "-i", str(bed_file)
                ], capture_output=True, text=True, check=True)
            out_path = out_dir / f"{bed_name}.{d}.bed"
            with open(out_path, 'w') as f:
                f.write(proc.stdout)


def summarize_clusters_and_slop_with_strand(clustered_dir, groupby_dir, slop_dir, genome_file):
    """
    For each clustered BED in clustered_dir, runs bedtools groupby and then bedtools slop
    to merge and pad cluster intervals. The slopped BED is placed in a subdir of slop_dir,
    named after the window (-d) value used.
    """
    clustered_dir = Path(clustered_dir)
    groupby_dir = Path(groupby_dir)
    slop_dir = Path(slop_dir)
    groupby_dir.mkdir(parents=True, exist_ok=True)
    slop_dir.mkdir(parents=True, exist_ok=True)
    
    for clustered_bed in clustered_dir.glob("*.bed"):
        try:
            d = int(clustered_bed.stem.split('.')[-1])
        except ValueError:
            print(f"Warning: Can't parse distance from {clustered_bed}. Skipping.")
            continue
        sample = ".".join(clustered_bed.stem.split('.')[:-1])
        grouped_bed = groupby_dir / f"{sample}.{d}.grouped.bed"

        # Make subdirectory for this -d value in slop_dir
        window_dir = slop_dir / str(d)
        window_dir.mkdir(exist_ok=True)
        slopped_bed = window_dir / f"{sample}.{d}.slop.bed"

        # Group by chrom, strand, cluster id, collect start, end, name, score
        groupby_cmd = [
            'bedtools', 'groupby',
            '-i', str(clustered_bed),
            '-g', '1,6,7',    # chrom, strand, cluster id
            '-c', '2,3,4,5',  # start, end, name, score
            '-o', 'min,max,first,first'
        ]
        proc_groupby = subprocess.run(groupby_cmd, capture_output=True, text=True, check=True)

        lines = []
        for line in proc_groupby.stdout.strip().split('\n'):
            fields = line.split('\t')
            if len(fields) < 7:
                continue
            # chrom, strand, cluster, min(start), max(end), name, score
            # BED6: chrom, min(start), max(end), name, score, strand
            lines.append('\t'.join([
                fields[0],         # chrom
                fields[3],         # min(start)
                fields[4],         # max(end)
                fields[5],         # name
                fields[6],         # score
                fields[1],         # strand
            ]))
        with open(grouped_bed, 'w') as f:
            for x in lines:
                f.write(f"{x}\n")

        # Run bedtools slop (strand will be honored if present in column 6)
        slop_cmd = [
            'bedtools', 'slop',
            '-i', str(grouped_bed),
            '-g', str(genome_file),
            '-b', str(d)
        ]
        proc_slop = subprocess.run(slop_cmd, capture_output=True, text=True, check=True)
        with open(slopped_bed, 'w') as f:
            f.write(proc_slop.stdout)


def TRIBE_pseudo_peak(bed, out_parent_dir, dist, genome_file, steps=None):
    """
    Runs clustering and groupby/slop summary to produce pseudo-peak BEDs.
    
    Args:
        bed:           Input BED file or path to BED directory/glob.
        out_parent_dir:Directory under which all output folders will be created.
        dist:          Maximum clustering distance (int).
        genome_file:   Chrom sizes file for bedtools slop.
        steps:         Optional step size for clusters (int or None). If given, runs clusters at step increments.
    """
    from pathlib import Path

    out_parent_dir = Path(out_parent_dir)
    clustered_dir = out_parent_dir / "clustered"
    groupby_dir = out_parent_dir / "grouped"
    slop_dir = out_parent_dir / "slopped"

    # Step 1: Cluster features
    clusters(bed, clustered_dir, dist, steps=steps)

    # Step 2: Summarize and run slop
    summarize_clusters_and_slop_with_strand(
        clustered_dir,
        groupby_dir,
        slop_dir,
        genome_file
    )
    print(f"Pseudo-peak workflow complete. Results in: {slop_dir}")

